/**
 * Namespace containing standard functions to manipulate texts/strings.
 */

namespace PHPStreamsAggregator\Library\Text;

/**
 * Get a string placed between two strings $start and $end.
 * @param   string - The text containing the part to extract
 * @param   string - The string used to define where the part to extract starts.
 * @param   string - The string used to define where the part to extract ends.
 * @returns string - The string extracted between $start and $end.
 */
function getBetween($string, $start, $end){
    $string = ' ' . $string;
    $ini = strpos($string, $start);
    if ($ini == 0) return '';
    $ini += strlen($start);
    $len = strpos($string, $end, $ini) - $ini;
    return substr($string, $ini, $len);
}